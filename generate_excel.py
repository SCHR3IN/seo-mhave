# Helper function to style headers
def style_header(ws, row_idx, headers):
    for col_idx, header in enumerate(headers, 1):
        cell = ws.cell(row=row_idx, column=col_idx, value=header)
        cell.fill = NAVY_HEADER_FILL
        cell.font = WHITE_HEADER_FONT
        cell.alignment = Alignment(horizontal="center", vertical="center", wrap_text=True)

# Helper to autofit column widths
def auto_fit_columns(ws, max_len_dict=None):
    ws.views.sheetView[0].showGridLines = True
    for col in ws.columns:
        col_letter = get_column_letter(col[0].column)
        max_len = 0
        for cell in col:
            val = str(cell.value or '')
            # split by newlines if any
            lines = val.split('\n')
            for line in lines:
                if len(line) > max_len:
                    max_len = len(line)
        # Apply cap to prevent massive widths
        if max_len_dict and col_letter in max_len_dict:
            ws.column_dimensions[col_letter].width = max_len_dict[col_letter]
        else:
            ws.column_dimensions[col_letter].width = min(max(max_len + 3, 12), 50)

